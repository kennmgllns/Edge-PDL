/*******************************************************************************//**
* \brief    Calculate the CRC-16 value.
*
* \details  This function is called to calculate the CRC-16 value of a
*           data packet to be sent.
*
* \b        Syntax <BR>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
*           crc16CalcByte(ui16_crc16, ui8_data)
* \param    ui16_crc16 -
* \param    ui8_data -
***********************************************************************************/
uint16_t crc16CalcByte(uint16_t ui16_crc16, uint8_t ui8_data)
{
    uint8_t  ui8_crc_byte_h = (uint8_t)((ui16_crc16 & 0xFF00) >> 8);
    uint8_t  ui8_crc_byte_l = (uint8_t)(ui16_crc16 & 0x00FF);
    uint8_t  ui8_index = 0;
    uint16_t ui16_crc  = 0;
    ui8_index       = ui8_crc_byte_h ^ ui8_data;
    ui8_crc_byte_h  = ui8_crc_byte_l ^ AUI8_CRC_LUT_H[ui8_index];
    ui8_crc_byte_l  = AUI8_CRC_LUT_L[ui8_index];
    ui16_crc = ((uint16_t)ui8_crc_byte_h << 8) | (uint16_t)ui8_crc_byte_l;
    return ui16_crc;
}

/*******************************************************************************//**
* \brief
*
* \details
*
* \b        Syntax <BR>&nbsp; &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;
*           crc16CalcBlock(pui8_data, ui16_length)
* \param    pui8_data -
* \param    ui16_length -
***********************************************************************************/
uint16_t crc16CalcBlock(uint8_t *pui8_data, uint16_t ui16_length)
{
    uint16_t    ui16_crc        = 0;
    uint8_t     ui8_crc_byte_h  = (UI16_CRC16_SEED_DEF & 0xFF00) >> 8;
    uint8_t     ui8_crc_byte_l  = (UI16_CRC16_SEED_DEF & 0x00FF);
    uint8_t     ui8_index       = 0;

    while(ui16_length > 0)
    {
        ui8_index       = ui8_crc_byte_h ^ *pui8_data++;
        ui8_crc_byte_h  = ui8_crc_byte_l ^ AUI8_CRC_LUT_H[ui8_index];
        ui8_crc_byte_l  = AUI8_CRC_LUT_L[ui8_index];
        ui16_length--;
    }

    ui16_crc = ((uint16_t)ui8_crc_byte_h << 8) | (uint16_t)ui8_crc_byte_l;
    return ui16_crc;
}

